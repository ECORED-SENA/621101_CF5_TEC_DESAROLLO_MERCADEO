            p Cross docking directo: es el que se produce cuando los paquetes (pallets, cajas, entre otros), previamente seleccionados por el proveedor de acuerdo con sus órdenes locales, son recibidos y transportados a las respectivas puertas de salida para consolidarlos con otros paquetes en el mismo formato, prácticamente sin que haya mayor manipulación. Las entregas son preparadas por el proveedor en función de cada uno de sus clientes finales mediante una unidad de carga específica y, consecutivamente, el centro de distribución identifica, clasifica y envía la mercadería al respectivo cliente (Papporello, 2013).
